# Создайте список случайных чисел.
# Найдите максимальное количество его одинаковых элементов

import random

list_num = []
max = 0

for i in range(10):
    a = random.randint(2, 5)
    list_num.append(a)

print(list_num)
# когда будем проходить по списку элементы могут повторяться и по ним повторно надо будет
# проходить. Ятобы этого не было используем множество
# в множестве элементы не повторяются
for i in set(list_num):
    if list_num.count(i) > max:
        max = list_num.count(i)
print(max)


