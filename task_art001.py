# Еще вариант решения с помощью списка
# Есть список с генерированными элементами и список длинны равной максимальному разбросу значений изначального списка, но заполненный 0
# Проходя по сгенерированному списку - взяли элемент и номеру соответсвтующему значению этого
# этого элемента в чистом списке присваиваем +1
# пройдясь по всему списку получим заполненный нулевый список где индекс списка соответсвтует
# значению элемента в сгенерированном списке, а значение нулевого списка у этого индекса
# соответсвтует количеству повторений 
#
import random
number = int(input())
maxRandom = int(input())
maxAmount = 0

list = []
count = [0] * (maxRandom+1)

for i in range(0, number):
    list.append(random.randit(0, maxRandom))

for i in list:
    count[int(i)] += 1

for i in count:
    if i > maxAmount:
        i = maxAmount
