# Другой вариант решения - создаем рандомный список и пцстой словарь
# по списку проходимся циклом и работаем со словарем так
# берется паервый элменты в списке. Если его нет в словаре, то он добавляется со значение 1
# Если элемент уже есть, то его значение изменятеся на +1
# возможно что то не работает. но смысл понятен

a = 1
b = 6

array = [random.randit(a, b) for i in range(20)]
print(array)

d = {}

for i in array:
    if i in d:
        d[i] += 1
    else:
        d[i] = 1

print(d)

max = 0
for i in range(a, (b + 1)):
    if (d[i] > max):
        max = d[i]
